$(document).ready(function(){
    $.getJSON("http://localhost:3000/flightenquiry/fetchallcities",function(data)
    {
        data.result.map((item)=>{
            $('#source').append($('<option>').text(item.cityname).val(item.citiesid))
            $('#destination').append($('<option>').text(item.cityname).val(item.citiesid))
        })
    })
})